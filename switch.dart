void main() {
  String fruit = "Mango";

  switch (fruit) {
    case "Apple":
      print("This is an Apple.");
      break;
    case "Banana":
      print("This is a Banana.");
      break;
    case "Mango":
      print("This is a Mango.");
      break;
    default:
      print("Unknown fruit.");
  }
}
